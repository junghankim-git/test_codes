!-------------------------------------------------------------------------------
   module w3odatmd
!-------------------------------------------------------------------------------
!
!  abstract: 
!
!  history log :
!    201x-0x-xx  your   name    code comment
!
!  structure:
!
!-------------------------------------------------------------------------------
   implicit none
   include 'mpif.h'
!
   integer :: iaproc, naproc
   public :: iaproc, naproc, initialize, finalize
!
   contains
!-------------------------------------------------------------------------------
!
!
!
!-------------------------------------------------------------------------------
   subroutine initialize()
!-------------------------------------------------------------------------------
   implicit none
!
   integer :: err
!
   call mpi_init(err)
   call mpi_comm_size(mpi_comm_world,naproc,err)
   call mpi_comm_rank(mpi_comm_world,iaproc,err)
   iaproc = iaproc+1
!
   end subroutine initialize
!-------------------------------------------------------------------------------
!
!
!
!-------------------------------------------------------------------------------
   subroutine finalize
!-------------------------------------------------------------------------------
   implicit none
!
   integer :: err
!
   call mpi_finalize(err)
!
   end subroutine finalize
!-------------------------------------------------------------------------------
!
!
!
!-------------------------------------------------------------------------------
   end module w3odatmd
!-------------------------------------------------------------------------------
